class Hitungan {
    static pembagian(a, b) {
        return a / b;
    }

    static pengurangan(a, b) {
        return a - b;
    }

    static perkalian(a, b) {
        return a * b;
    }

    static penjumlahan(a, b) {
        return a + b;
    }
}

module.exports = Hitungan;

